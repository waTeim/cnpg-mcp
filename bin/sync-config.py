#!/usr/bin/env python3
"""
Regenerate `make.env` from `mcp-project.yaml`.

`mcp-project.yaml` is the single source of truth for project-level defaults
(image names, ports, Helm release, namespace, ...). The Makefile refreshes
`make.env` automatically when this config changes; this script is the manual
entry point for that same regeneration.

The generated `make.env` is intended to be regeneratable — do NOT edit it
by hand. Edit `mcp-project.yaml` and re-run this script.

Helm release values (`<helmRelease>.yaml` at repo root) are deployment
artifacts and are NOT generated by this script — edit them directly.

Usage:
    python bin/sync-config.py [--config <path>] [--output <path>]
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    print("error: PyYAML is required. Install with:", file=sys.stderr)
    print("  pip install -r requirements.txt", file=sys.stderr)
    sys.exit(2)


REPO_ROOT = Path(__file__).resolve().parent.parent


def _require(d: dict, *path: str) -> object:
    """Walk a dotted path through dicts; raise KeyError on missing keys."""
    cur = d
    for k in path:
        if not isinstance(cur, dict) or k not in cur:
            raise KeyError(".".join(path))
        cur = cur[k]
    return cur


def _derive_helm_service(release: str, chart_name: str) -> str:
    """
    Mirror Helm's default fullname template logic:
      release == chart_name           → release
      release contains chart_name     → release  (already qualified)
      otherwise                       → release-chart_name
    """
    if release == chart_name or chart_name in release:
        return release
    return f"{release}-{chart_name}"


def render_make_env(config: dict) -> str:
    """Render a make.env body from the parsed mcp-project.yaml."""
    project       = _require(config, "project")
    ports         = _require(config, "ports")
    build         = _require(config, "build")
    deployment    = _require(config, "deployment")

    chart_name    = _require(project, "chartName")
    main_port     = int(_require(ports, "main"))
    test_port     = int(_require(ports, "test"))
    helm_release  = _require(deployment, "helmRelease")
    namespace     = _require(deployment, "namespace")

    helm_service  = _derive_helm_service(str(helm_release), str(chart_name))

    lines = [
        "# Auto-generated by bin/sync-config.py from mcp-project.yaml.",
        "# Do NOT edit by hand — edit mcp-project.yaml; Make refreshes this file.",
        "",
        f"REGISTRY = {_require(build, 'registry')}",
        f"IMAGE_NAME = {_require(build, 'imageName')}",
        f"TEST_IMAGE_NAME = {_require(build, 'testImageName')}",
        f"TAG = {_require(build, 'tag')}",
        f"PLATFORM = {_require(build, 'platform')}",
        f"CONTAINER_TOOL = {_require(build, 'containerTool')}",
        f"HELM_RELEASE = {helm_release}",
        f"HELM_NAMESPACE = {namespace}",
        f"HELM_CHART_NAME = {chart_name}",
        f"HELM_SERVICE = {helm_service}",
        f"MCP_PORT = {main_port}",
        f"MCP_TEST_PORT = {test_port}",
        "",
    ]
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--config", type=Path, default=REPO_ROOT / "mcp-project.yaml",
        help="Path to mcp-project.yaml (default: repo root)",
    )
    parser.add_argument(
        "--output", type=Path, default=REPO_ROOT / "make.env",
        help="Output path (default: ./make.env at repo root)",
    )
    args = parser.parse_args()

    if not args.config.exists():
        print(f"error: config not found: {args.config}", file=sys.stderr)
        return 1

    with args.config.open("r", encoding="utf-8") as f:
        try:
            config = yaml.safe_load(f) or {}
        except yaml.YAMLError as e:
            print(f"error: invalid YAML in {args.config}: {e}", file=sys.stderr)
            return 1

    try:
        body = render_make_env(config)
    except KeyError as e:
        print(f"error: missing required key in {args.config}: {e}", file=sys.stderr)
        return 1
    except (TypeError, ValueError) as e:
        print(f"error: invalid value in {args.config}: {e}", file=sys.stderr)
        return 1

    args.output.write_text(body, encoding="utf-8")
    print(f"✓ Wrote {args.output} from {args.config}")
    return 0


if __name__ == "__main__":
    sys.exit(main())